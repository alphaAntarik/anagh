package com.etechnie.anagh.models.doctor_model;

public class RequestDoctor {
    private Integer id;
    private String icon;
    private String image;
    private String name;
    private String description;
    private Integer experience;
    private String contact_no;
    private String email;
    private String address;
    private String fees;
    private Integer clinic_id;
    private String speciality;
    private String created_date;
    private Integer status;
    private Integer chamber_type;
    private String degree;
    public Integer getId() {
        return id;
    }
    public void setId(Integer id) {
        this.id = id;
    }
    public String getIcon() {
        return icon;
    }
    public void setIcon(String icon) {
        this.icon = icon;
    }
    public String getImage() {
        return image;
    }
    public void setImage(String image) {
        this.image = image;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getDescription() {
        return description;
    }
    public void setDescription(String description) {
        this.description = description;
    }
    public Integer getExperience() {
        return experience;
    }
    public void setExperience(Integer experience) {
        this.experience = experience;
    }
    public String getContactNo() {
        return contact_no;
    }
    public void setContactNo(String contactNo) {
        this.contact_no = contactNo;
    }
    public String getEmail() {
        return email;
    }
    public void setEmail(String email) {
        this.email = email;
    }
    public String getAddress() {
        return address;
    }
    public void setAddress(String address) {
        this.address = address;
    }
    public String getFees() {
        return fees;
    }
    public void setFees(String fees) {
        this.fees = fees;
    }
    public Integer getClinicId() {
        return clinic_id;
    }
    public void setClinicId(Integer clinicId) {
        this.clinic_id = clinicId;
    }
    public String getSpeciality() {
        return speciality;
    }
    public void setSpeciality(String speciality) {
        this.speciality = speciality;
    }
    public String getCreatedDate() {
        return created_date;
    }
    public void setCreatedDate(String createdDate) {
        this.created_date = createdDate;
    }
    public Integer getStatus() {
        return status;
    }
    public void setStatus(Integer status) {
        this.status = status;
    }
    public Integer getChamberType() {
        return chamber_type;
    }
    public void setChamberType(Integer chamberType) {
        this.chamber_type = chamberType;
    }
    public String getDegree() {
        return degree;
    }
    public void setDegree(String degree) {
        this.degree = degree;
    }
}
